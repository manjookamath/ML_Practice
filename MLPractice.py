def make_dataset(X_paths, y_labels, is_training=False):
    """Creates a batched and optimized tf.data.Dataset."""

    # Create a dataset from the NumPy arrays of file paths and labels
    dataset = tf.data.Dataset.from_tensor_slices((X_paths, y_labels))

    # Apply the parsing function to every element in the dataset
    dataset = dataset.map(parse_image, num_parallel_calls=AUTOTUNE)

    if is_training:
        # For training: shuffle and apply data augmentation
        dataset = dataset.shuffle(buffer_size=1000) # Shuffle the dataset
        dataset = dataset.repeat() # Repeat the dataset indefinitely for Keras model.fit

    # Batch the elements
    dataset = dataset.batch(BATCH_SIZE)

    # Prefetch the next batch for optimization
    dataset = dataset.prefetch(AUTOTUNE)

    return dataset

