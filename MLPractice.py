def extract_features(image_path_list):
    """ Loads images and extracts features (3D Color Histogram) for SVM """
    feature_vectors = []
    bins = 8 # Number of bins for the histogram in each channel

    for path in image_path_list:
        try:
            img = imread(path)

            # Ensure the image is 3-channel (RGB)
            if img.ndim == 2:
                img = color.gray2rgb(img)
            elif img.shape[2] == 4:
                img = img[:, :, :3]

            img = cv2.resize(img, (256, 256))

            # Calculate the 3D histogram
            hist = cv2.calcHist([img], [0, 1, 2], None, [bins, bins, bins],[0, 256, 0, 256, 0, 256])

            # Normalize and flatten
            hist = hist / np.sum(hist)
            feature_vector = hist.flatten()
            feature_vectors.append(feature_vector)

        except Exception as e:
            # Handle potential loading/processing errors gracefully
            print(f"Skipping image {path} due to error: {e}")

    return np.array(feature_vectors)

