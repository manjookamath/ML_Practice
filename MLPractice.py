# Image Loading and Preprocessing Function
def parse_image(file_path, label):
    """Loads, decodes, resizes, and normalizes an image."""

    # Python function to handle image loading and decoding using PIL
    def _decode_image_py_func(path_tensor):
        path = path_tensor.numpy().decode('utf-8') # Convert TF tensor string to Python string

        try:
            img_pil = Image.open(path)

            # Ensure image is in RGB format
            if img_pil.mode == 'RGBA':
                img_pil = img_pil.convert('RGB')
            elif img_pil.mode == 'L': # Grayscale to RGB
                img_pil = img_pil.convert('RGB')
            elif img_pil.mode != 'RGB': # Handle other non-standard modes by converting to RGB
                 img_pil = img_pil.convert('RGB')

            img_np = np.array(img_pil)
        except Exception as e:
            # Fallback for robustness, though PIL should handle common TIFFs.
            print(f"Warning: PIL failed to load image {path} with error: {e}. Attempting tf.io.decode_image as fallback.")
            img_bytes = tf.io.read_file(path)
            img_np = tf.io.decode_image(img_bytes, channels=3, expand_animations=False).numpy()

        return img_np

    # Use tf.py_function to wrap the Python decoding logic
    img = tf.py_function(func=_decode_image_py_func, inp=[file_path],Tout=tf.uint8)
    # Set the shape explicitly. The dimensions (height, width) can vary before resize.
    img.set_shape([None, None, 3])

    # Resize the image
    img = tf.image.resize(img, [IMG_SIZE, IMG_SIZE])

    # Convert to float32 and normalize to [0, 1]
    img = tf.image.convert_image_dtype(img, tf.float32)

    # Convert the integer label to one-hot encoding for classification
    label = tf.one_hot(label, NUM_CLASSES)

    return img, label

